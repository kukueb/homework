number_1 = int(input('Первое число: '))
number_2 = int(input('Второе число: '))

summ = number_1 + number_2 
diff = number_1 - number_2
mult = number_1 * number_2 # Умножение
div = number_1 / number_2 # Деление

print('Сумма: ', summ) 
print('Разность: ' , diff)
print('Произведение:', mult) # Вывод умножения
print('Частное:', div) # Вывод деления